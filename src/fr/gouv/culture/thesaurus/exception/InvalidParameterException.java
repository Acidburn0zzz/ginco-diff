package fr.gouv.culture.thesaurus.exception;

/**
 * A business exception notifying that one of the HTTP request
 * parameter, either in path, query or form, is invalid (missing,
 * out of range, of the wrong type...).
 */
public class InvalidParameterException extends BusinessException
{
    public InvalidParameterException(String message) {
        super(message);
    }

    public InvalidParameterException(String message, Throwable cause) {
        super(message, cause);
    }

    public InvalidParameterException(Throwable cause) {
        super(cause);
    }

    public InvalidParameterException(String format, Object[] data) {
        super(format, data);
    }

    public InvalidParameterException(String format, Object[] data,
                                                    Throwable cause) {
        super(format, data, cause);
    }
}
